    private void rutFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_rutFocusLost
        if(!rut.getText().isEmpty()){
            int M=0,S=1,T=Integer.parseInt(rut.getText()); 
            for(;T!=0;T/=10)S=(S+T%10*(9-M++%6))%11; 
            String digito = Character.toString(((char)(S!=0?S+47:75)));
            digitoVerificador.setText(digito);
        }else{
            digitoVerificador.setText("");
        }
    }//GEN-LAST:event_rutFocusLost

